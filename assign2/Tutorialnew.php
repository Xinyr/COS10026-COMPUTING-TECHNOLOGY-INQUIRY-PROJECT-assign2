                    <h3 class="tutorialH3">Mounting the leaves</h3>
                    <hr>
                    <p class="tutorialPara">Once the leaves are dried, mount them on a piece of acid-free paper and lay them flat or to the position that you desired. Then, brush it with adhesive to hold the leaves positions while the adhesive protect it from insects, fungus and mold hence prolong it's preservation</p>
            </section>
            <section>
                <h3 class="tutorialH3">Catalog and storage</h3>
                <hr>
                <p class="tutorialPara">Once everything is done, your final product should be stored in a safe place. Place it within acid-free plastic sleeve and label the sleeve with important data such as leaves botanical name, common name, date of collection and identification number. The sleeve should later be stored in a safe place such as</p>
