                    <h3 class="tutorialH3">Preparing the leaves</h3>
                    <hr>
                    <p class="tutorialPara">After reaching home, wash the leaves with clean water to remove dust and dirt. Then, remove excess moisture by wiping the leaves with a paper towel. After that, place a piece of parchment paper above and below the leaves before pressing. Also remember to arrange carefully so no leaves overlap each others to make sure all leaves could dry off evenly.</p>
            </section>
            <section>
                    <h3 class="tutorialH3">Pressing the leaves</h3>
                    <hr>
                    <p class="tutorialPara">The next step would be placing the leaves that are within the layer of parchment paper into the herbarium press. After the press is fully loaded, start apply pressure to make the leaves flat and fasten the drying. Make sure the leaves are flat enough or else it will bubbling. Use suitable items for pressing such as heavy books, but do beware not to put too heavy object for pressing as it will crush and damage the leaves.</p>
            </section>
